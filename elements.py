class element:
    def __init__(self, symbol, name, mass, family, species):
        self.symbol = symbol
        self.mass = mass
        self.family = family
        self.species = species
        self.name = name

    def get_mass(self):
        return self.mass

    def get_symbol(self):
        return self.symbol

    def get_family(self):
        return self.family

